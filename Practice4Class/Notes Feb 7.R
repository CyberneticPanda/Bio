# Intro to version control

## Why version control systems?
-Efficient way of keeping trace of changes in a document.
-Kepp Tract of changes in any type of document: txt, table, etc

## Conflicts in version control
- Conflict happens when someone changes the same part of a document at the same 
  time

## What is a commit
- Saves the changes made to files to a repository
- It does not have to be all files at a time
  
## What is a repository?
- It is the whole of files and file versions, along with history of changes made
  (commits)
  
## Difference between old and modern version control systems
= Centralization vs distribution

